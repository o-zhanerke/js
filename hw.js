let age = prompt("Жасыңызды енгізіңіз:");

if (age >= 18) {
  alert("Сіз ересексіз!");
} else {
  alert("Сіз кәмелетке толмағансыз!");
}

let temperature = prompt("Сырттағы температураны енгізіңіз (°C):");

if (temperature > 25) {
  alert("Жеңіл шорты және футболка киюге болады.");
} else if (temperature > 0) {
  alert("Жеңіл күртеше немесе жемпір киіңіз.");
} else {
  alert("Қалың куртка мен жемпір киіңіз, қалаңырақ киініңіз.");
}
